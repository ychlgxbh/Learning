class CurvePathInfo {
  final currentX;
  final currentY;
  final refX;
  final refY;
  final destX;
  final destY;
  final direction;
  final curveWeight;

  CurvePathInfo({
    this.currentX,
    this.currentY,
    this.refX,
    this.refY,
    this.destX,
    this.destY,
    this.direction,
    this.curveWeight,
  });
}
